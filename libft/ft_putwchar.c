#include "libft.h"

void	ft_putwchar(wchar_t c)
{
	char s[4];

	if ((c >= 0 && c <= 127) || (c >= 0 && c <= 255 && MB_CUR_MAX == 1))
		write(1, &c, 1);
	else if (c >= 128 && c <= 2047 && MB_CUR_MAX >= 2)
	{
		s[0] = ((c >> 6) + 192);
		s[1] = ((c & 63) + 128);
		write(1, s, 2);
	}
	else if (c >= 2048 && c <= 65535 && MB_CUR_MAX >= 3)
	{
		s[0] = ((c >> 12) + 224);
		s[1] = (((c >> 6) & 63) + 128);
		s[2] = ((c & 63) + 128);
		write(1, s, 3);
	}
	else if (c >= 65536 && c <= 1114111 && MB_CUR_MAX == 4)
	{
		s[0] = ((c >> 18) + 240);
		s[1] = (((c >> 12) & 63) + 128);
		s[2] = (((c >> 6) & 63) + 128);
		s[3] = ((c & 63) + 128);
		write(1, s, 4);
	}
}
